    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        Properties prop = new Properties();
        try {
            output = new FileOutputStream("dbd.properties");
            prop.setProperty("db",rutaBD.getText());
            prop.setProperty("servidor", servidor.getText());
            prop.setProperty("root", "jdbc:firebirdsql:");
            prop.setProperty("user", "SYSDBA");
            prop.setProperty("password", "masterkey");
            // save properties to project root folder
		prop.store(output, null);
        JOptionPane.showMessageDialog(this,"RUTA CAMBIADA CON EXITO\nSE RECOMIENDA REINICIAR LA PC",  "NotificacionesCise",JOptionPane.INFORMATION_MESSAGE);
        
        } catch (IOException io) {
            JOptionPane.showMessageDialog(this,"ERROR\nASEGURESE DE TENER LOS PRIVILEGIOS",  "NotificacionesCise",JOptionPane.ERROR);
		io.printStackTrace();
        
        }finally {
		if (output != null) {
			try {
				output.close();
			} catch (IOException e) {
				e.printStackTrace();
			}
		}
        }
       
    }//GEN-LAST:event_jButton1ActionPerformed

